#include <bits/stdc++.h>
using namespace std;


int main(void)
{
	ios::sync_with_stdio(false);
	cin.tie(nullptr);

	while (!cin.eof())
	{
		string str;
		getline(cin, str);

		cout << str << "\n";
	}

	return 0;
}